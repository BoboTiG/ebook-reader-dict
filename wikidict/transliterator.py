def transliterate(locale: str, text: str) -> str:
    """
    Return the transliterated form of *text*.

        >>> transliterate("ar", "بطيخ")
        'bṭīḫ'
        >>> transliterate("bg", "карам велосипед")
        'karam velosiped'
        >>> transliterate("by", "")
        ''
        >>> transliterate("el", "α")
        'a'
        >>> transliterate("hy", "Գաֆեսճյան")
        'Gafesč̣yan'
        >>> transliterate("hi", "अ")
        'a'
        >>> transliterate("kk", "Көкшетау")
        'Kökşetaw'
        >>> transliterate("mk", "војна")
        'vojna'
        >>> transliterate("rif", "")
        ''
        >>> transliterate("ru", "дед")
        'ded'
        >>> transliterate("shi", "")
        ''
        >>> transliterate("sr", "апсолутна нула")
        'apsolutna nula'
        >>> transliterate("tzm", "ⵜⴰⵎⴰⵣⵉⵖⵜ")
        'tamaziɣt'
        >>> transliterate("uk", "арабська мова")
        'arabsʹka mova'
        >>> transliterate("zgh", "ⵜⴰⴳⵍⴷⵉⵜ ⵏ ⵍⵎⵖⵔⵉⴱ")
        'tagldit n lmɣrib'

        >>> transliterate("unknown", "foo")
        ''
    """
    if table := TRANSLITERATIONS.get(locale):
        common = TRANSLITERATIONS["common"]
        return "".join(common.get(char) or table.get(char, "") for char in text)
    return ""
